<template>
    <transition name="confirm-fade" @click.stop>
        <div class="confirm" v-show="showFlag">
            <div class="confirm-wrapper">
                <div class="confirm-content">
                    <p class="text">{{text}}</p>
                    <div class="operate">
                        <div  @click.stop="cancel" class="operate-btn left">{{cancelText}}</div>
                        <div  @click.stop="confirm" class="operate-btn">{{confirmText}}</div>
                    </div>
                </div>
            </div>
        </div>
    </transition>  
</template>
<script type="text/ecmascript-6">
export default {
  data() {
    return {
      showFlag: false
    }
  },
  props: {
    text: {
      type: String,
      default: ''
    },
    confirmText: {
      type: String,
      default: '清空'
    },
    cancelText: {
      type: String,
      default: '取消'
    }
  },
  methods: {
    show() {
      this.showFlag = true
    },
    hide() {
      this.showFlag = false
    },
    cancel() {
      this.hide()
      this.$emit('cancel')
    },
    confirm() {
      this.hide()
      this.$emit('confirm')
    }
  }
}
</script>
